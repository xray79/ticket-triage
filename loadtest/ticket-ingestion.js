// Concurrent ticket-ingestion load test against POST /api/tickets — see README.md for
// why this uses autocannon instead of k6 (the plan's own suggestion allows "k6 or similar").
const autocannon = require('autocannon');

const BASE_URL = process.env.LOADTEST_BASE_URL || 'http://localhost:5000';
const EMAIL = process.env.LOADTEST_EMAIL || 'admin@ticket-triage.local';
const PASSWORD = process.env.LOADTEST_PASSWORD || 'ChangeMe123!';
const DURATION_SECONDS = Number(process.env.LOADTEST_DURATION_SECONDS || 10);
const CONNECTION_LEVELS = (process.env.LOADTEST_CONNECTIONS || '5,20,50,100')
  .split(',')
  .map((n) => Number(n.trim()));

async function login() {
  const response = await fetch(`${BASE_URL}/api/auth/login`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ email: EMAIL, password: PASSWORD }),
  });
  if (!response.ok) {
    throw new Error(`Login failed: ${response.status} ${await response.text()}`);
  }
  const body = await response.json();
  return body.accessToken;
}

function runOnce(connections, token) {
  return new Promise((resolve, reject) => {
    const instance = autocannon(
      {
        url: `${BASE_URL}/api/tickets`,
        method: 'POST',
        connections,
        duration: DURATION_SECONDS,
        headers: {
          'content-type': 'application/json',
          authorization: `Bearer ${token}`,
        },
        setupClient: (client) => {
          client.setBody(
            JSON.stringify({
              subject: 'Load test ticket',
              body: 'Generated by loadtest/ticket-ingestion.js — see docs/load-test-report.md.',
              customerEmail: 'loadtest-customer@example.com',
            })
          );
        },
      },
      (err, result) => (err ? reject(err) : resolve(result))
    );
    autocannon.track(instance, { renderProgressBar: false });
  });
}

function statusCount(result, code) {
  const stat = result.statusCodeStats?.[code];
  return stat ? stat.count : 0;
}

async function main() {
  console.log(`Logging in as ${EMAIL} against ${BASE_URL}...`);
  const token = await login();
  console.log('Login succeeded.\n');

  const rows = [];
  for (const connections of CONNECTION_LEVELS) {
    console.log(`\n=== ${connections} concurrent connections, ${DURATION_SECONDS}s ===`);
    const result = await runOnce(connections, token);
    const row = {
      connections,
      requestsPerSec: result.requests.average,
      latencyP50: result.latency.p50,
      latencyP99: result.latency.p99,
      errors: result.errors,
      timeouts: result.timeouts,
      status2xx: statusCount(result, 201) + statusCount(result, 200),
      status429: statusCount(result, 429),
      statusOther:
        result.requests.total - statusCount(result, 201) - statusCount(result, 200) - statusCount(result, 429),
    };
    rows.push(row);
    console.log(
      `req/s=${row.requestsPerSec} p50=${row.latencyP50}ms p99=${row.latencyP99}ms ` +
        `2xx=${row.status2xx} 429=${row.status429} other=${row.statusOther} errors=${row.errors}`
    );
  }

  console.log('\n\n| Connections | Req/s | p50 (ms) | p99 (ms) | 2xx | 429 | Other | Errors |');
  console.log('|---|---|---|---|---|---|---|---|');
  for (const row of rows) {
    console.log(
      `| ${row.connections} | ${row.requestsPerSec} | ${row.latencyP50} | ${row.latencyP99} | ` +
        `${row.status2xx} | ${row.status429} | ${row.statusOther} | ${row.errors} |`
    );
  }
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
